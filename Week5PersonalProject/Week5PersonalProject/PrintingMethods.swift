func printYutBoard(_ currentYutBoard: [[String]]) {
    var a = 0
    for i in 0..<currentYutBoard.count {
        if currentYutBoard[i].first != "  " {
            if a == 5 {
                print("                                            \(2*a)",terminator: "")
            } else {
                print("                                             \(2*a)",terminator: "")
            }
            a += 1
        }else {
            print("                                              ",terminator: "")
        }
        for j in 0..<currentYutBoard[0].count {
            print(currentYutBoard[i][j], terminator: "")
        }
        print("")
    }
    print("                                              0      3     6     9     12    15")
    print("")
}

